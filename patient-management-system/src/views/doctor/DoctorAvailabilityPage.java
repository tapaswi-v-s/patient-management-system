    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        boolean response = doctorController.addAppointments(
                txtDate.getDate(), 
                (String)drpStartTime.getSelectedItem(),
                (String)drpEndTime.getSelectedItem());
        if(response){
            Utils.showDialog(this, "Appointment added successfully...!", null);
        }else{
            Utils.showDialog(this, null, "Something Went Wrong :(");
        }
    }//GEN-LAST:event_btnAddActionPerformed

