    void populateFields(){
        txtDate.setMinSelectableDate(new Date());
        txtDate.setDate(new Date());
        DefaultListModel<String> listModel = new DefaultListModel<>();
        appointments = doctorController.getMyAppointments(txtDate.getDate());
        listModel.addAll(appointments.stream()
                .map(a -> a.getTimeSlot() + " "+ a.getPatient().getfName() + " " + a.getPatient().getlName()).toList());
        lstPatients.setModel(listModel);
    }

