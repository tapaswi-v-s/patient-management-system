    void populate(){
        DefaultListModel<String> listModel = new DefaultListModel<>();
        doctors = adminController.getAllDoctors();
        listModel.addAll(doctors.stream()
                .map(d -> d.getfName() + " "+d.getlName() + ", " + d.getType().name()).toList());
        lstDoctor.setModel(listModel);
    }

