    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        if(appointment != null){
            String timeSlot = ((String)drpStartTime.getSelectedItem())+" - "+((String)drpEndTime.getSelectedItem());
            appointment.setTimeSlot(timeSlot);
            appointment.setDate(txtDate.getDate());
            if(adminController.updateActiveAppointment(appointment)){
            Utils.showDialog(
                    this,
                    "Appointment Updated Successfully...", 
                    null);
            }else{
                Utils.showDialog(
                        this,
                        null, 
                        "This Appointment is not active!");
            }
        }
    }//GEN-LAST:event_btnSubmitActionPerformed


