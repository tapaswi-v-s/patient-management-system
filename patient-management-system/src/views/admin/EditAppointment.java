    void populate(){
        if(appointment != null){
            lblDocName.setText(appointment.getDoctor().getfName() + " " + appointment.getDoctor().getlName());
            lblDocType.setText(appointment.getDoctor().getType().name());
            lblHospital.setText(appointment.getHospital().getName());

            if(appointment.getPatient() != null){
                lblPName.setText(appointment.getPatient().getfName() + " " + appointment.getPatient().getlName());
                lblbloodGroup.setText(appointment.getPatient().getBloodGroup());
                lblAge.setText(appointment.getPatient().getAge()+"");
            }else{
                lblPName.setText("Not Available");
                lblbloodGroup.setText("Not Available");
                lblAge.setText("Not Available");
            }
            
            String startTime = appointment.getTimeSlot().split("-")[0].trim();
            String endTime = appointment.getTimeSlot().split("-")[1].trim();
            
            System.out.println(startTime+" "+StringUtils.getTimings().indexOf(startTime)+"\n"
                    +endTime+" "+StringUtils.getTimings().indexOf(endTime));
            
            drpStartTime.setSelectedItem(startTime);
            drpEndTime.setSelectedItem(endTime);
            
            txtDate.setDate(appointment.getDate());
        }
    }

