    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        if(hospital != null){
            hospital.setName(txtName.getText());
            List<String> cities = List.of(txtCities.getText().split(","));
            hospital.setCities(cities.stream().map(String::trim).toList());
            if(adminController.updateHospital(hospital)){
                Utils.showDialog(this, "Hospital Updated...!", null);
            }else{
                Utils.showDialog(this, null, "Something Went Wrong!");                
            }
        }else{
            if(adminController.addHospital(new Hospital(
                    txtName.getText(),
                    List.of(txtCities.getText().split(","))))){
                Utils.showDialog(this, "New Hospital Added...!", null);
            }else{
                Utils.showDialog(this, null, "Something Went Wrong!");                
            }
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        bottomPanel.remove(this);
        CardLayout cl = (CardLayout) bottomPanel.getLayout();
        cl.previous(bottomPanel);
    }//GEN-LAST:event_btnBackActionPerformed


