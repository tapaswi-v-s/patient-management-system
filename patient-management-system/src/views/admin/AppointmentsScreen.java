    void populate(){
        DefaultListModel<String> listModel = new DefaultListModel<>();
        appointments = adminController.getAllAppointments();
        listModel.addAll(appointments.stream()
                .map(a -> a.getTimeSlot()+"  ||  "+Utils.sdf.format(a.getDate())).toList());
        lstAppointment.setModel(listModel);
    }

