    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        bottomPanel.remove(this);
        CardLayout cl = (CardLayout) bottomPanel.getLayout();
        cl.previous(bottomPanel);
    }//GEN-LAST:event_btnBackActionPerformed

    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        if(lstAppointment.getSelectedIndex() < 0){
            Utils.showDialog(this, null, "Please Select the Appointment!");
        }else{
            bottomPanel.add(new EditAppointment(bottomPanel, adminController,
                appointments.get(lstAppointment.getSelectedIndex())));
            CardLayout cl = (CardLayout) bottomPanel.getLayout();
            cl.next(bottomPanel);
        }

    }//GEN-LAST:event_btnViewActionPerformed

    private void btnRefreshActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRefreshActionPerformed
        populate();
    }//GEN-LAST:event_btnRefreshActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if(lstAppointment.getSelectedIndex() < 0){
            Utils.showDialog(this, null, "Please Select the Appointment!");
        }else{
            if(adminController.deleteAppointment(
                    appointments.get(lstAppointment.getSelectedIndex()))){
                Utils.showDialog(
                        this,
                        "Appointment Removed Successfully...", 
                        null);
                populate();
            }else{
                Utils.showDialog(
                        this,
                        null, 
                        "Something went wrong!");
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed


