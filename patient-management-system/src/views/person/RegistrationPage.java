    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        
        Patient patient = new Patient();
        patient.setfName(txtFName.getText());
        patient.setlName(txtLName.getText());
        patient.setBloodGroup(txtBloodGroup.getText());
        patient.setAge(Double.parseDouble(txtAge.getText()));
        patient.setUserName(txtUserName.getText());
        patient.setPassword(txtPassword.getText());
        patient.setGender(Gender.valueOf((String)drpGender.getSelectedItem()));
        patient.setAddress(new Address(
                txtHouseName.getText(), 
                new City(txtCity.getText(),txtCommunity.getText())));
        patient.setUserType(UserType.PATIENT);
        
        boolean response = personController.register(appointment, patient);
        
        if(response){
            Utils.showDialog(this, "Registration Success...!", null);
        }else{
            Utils.showDialog(this, null, "Something Went Wrong!");
        }
        
        
        bottomPanel.removeAll();
        FirstScreen firstScreen = new FirstScreen(bottomPanel);
        bottomPanel.add(firstScreen);
        CardLayout cl = (CardLayout) bottomPanel.getLayout();
        cl.next(bottomPanel);
        
    }//GEN-LAST:event_btnRegisterActionPerformed


